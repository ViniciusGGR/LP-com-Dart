void main() {
  double a;
  a = 1.91;

  // Tipos de declaração de uma variável 'double'.

  // Tipo dinâmico:
  var b;
  b = 1.89;

  // Tipo dinâmico com valor inicial já atribuido:
  var c = 1.91;
  print(c);

  // Tipo explícito:
  double d;
  d = 78.5;

  // Tipo explícito com valor inicial já atribuido:
  double e = 78.6;
  print(e);
}